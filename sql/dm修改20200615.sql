COMMENT ON COLUMN "RY"."DM_COST"."COST_APPLY_TIME" IS '申请支付时间';
COMMENT ON COLUMN "RY"."DM_COST"."COST_PAY_TIME" IS '实际支付时间';
COMMENT ON COLUMN "RY"."DM_COST"."COST_WATER_VOLUME" IS '实际处理水量';
COMMENT ON COLUMN "RY"."DM_COST"."COST_PAY_MONEY" IS '实付处理费';
COMMENT ON COLUMN "RY"."DM_COST"."COST_PUNISH_MONEY" IS '处罚金额';
COMMENT ON COLUMN "RY"."DM_COST"."COST_PUNISH_REASON" IS '处罚原因';
COMMENT ON COLUMN "RY"."DM_COST"."COST_REMARK" IS '备注';
COMMENT ON COLUMN "RY"."DM_COST"."COST_FILE_NAME" IS '文件名';
COMMENT ON COLUMN "RY"."DM_COST"."COST_FILE_PATH" IS '文件相对路径';
COMMENT ON COLUMN "RY"."DM_COST"."CREATE_TIME" IS '创建时间';


package com.szps.web.domain.event;

public class EventPicture {


    private String eventSid;

    private String eventPictureName;

    private String eventPictureUrl;

    public String getEventSid() {
        return eventSid;
    }

    public void setEventSid(String eventSid) {
        this.eventSid = eventSid;
    }

    public String getEventPictureName() {
        return eventPictureName;
    }

    public void setEventPictureName(String eventPictureName) {
        this.eventPictureName = eventPictureName;
    }

    public String getEventPictureUrl() {
        return eventPictureUrl;
    }

    public void setEventPictureUrl(String eventPictureUrl) {
        this.eventPictureUrl = eventPictureUrl;
    }
}

CREATE TABLE "RY"."DM_COST" (
"COST_ID" NUMBER(20) NOT NULL ,
"COST_FACTORY" VARCHAR2(100 BYTE) NULL ,
"COST_COMPANY" VARCHAR2(100 BYTE) NULL ,
"COST_APPLY_TIME" DATE NULL ,
"COST_PAY_TIME" VARCHAR2(30 BYTE) NULL ,
"COST_WATER_VOLUME" NUMBER(20) NULL ,
"COST_PAY_MONEY" NUMBER(20) NULL ,
"COST_PUNISH_MONEY" NUMBER(20) NULL ,
"COST_PUNISH_REASON" VARCHAR2(255 BYTE) NULL ,
"COST_REMARK" VARCHAR2(255 BYTE) NULL ,
"COST_FILE_NAME" VARCHAR2(255 BYTE) NULL ,
"COST_FILE_PATH" VARCHAR2(255 BYTE) NULL ,
"CREATE_TIME" DATE NULL 
)
LOGGING
NOCOMPRESS
NOCACHE;


COMMENT ON COLUMN "RY"."DM_COST"."COST_FACTORY" IS '水质净化厂';
